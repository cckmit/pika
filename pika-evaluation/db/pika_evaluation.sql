DROP TABLE IF EXISTS `item_evaluation`;
CREATE TABLE `item_evaluation` (
  `id` bigint(20) NOT NULL COMMENT 'ID',
  `buyer_id` bigint(20) DEFAULT NULL COMMENT '买家ID',
  `buyer_name` varchar(128) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '买家名称',
  `seller_id` bigint(20) DEFAULT NULL COMMENT '卖家ID',
  `seller_id` bigint(20) DEFAULT NULL COMMENT '商家ID',
  `seller_code` varchar(128) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '商家编码',
  `seller_name` varchar(128) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '商家名称',
  `order_detail_id` bigint(20) DEFAULT NULL COMMENT '交易订单ID',
  `order_detail_code` varchar(128) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '交易订单Code',
  `order_status` varchar(32) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '一般只会是售后状态或者已完成',
  `item_id` bigint(20) DEFAULT NULL COMMENT '商品ID',
  `item_code` varchar(128) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '商品编码',
  `item_name` varchar(128) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '商品名称',
  `sale_sku_id` bigint(20) DEFAULT NULL COMMENT '销售SKU',
  `sku_name` varchar(128) COLLATE utf8mb4_bin DEFAULT NULL COMMENT 'SKU名',
  `sku_info` varchar(128) COLLATE utf8mb4_bin DEFAULT NULL COMMENT 'SKU简述属性拼接，如：金色；128G',
  `evaluation_type` varchar(32) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '正常评价 or 追评',
  `evaluation_filter_type` varchar(32) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '评价筛选类型',
  `score` int(11) DEFAULT NULL COMMENT '追评时候为默认分数',
  `content` text COLLATE utf8mb4_bin COMMENT '评价内容',
  `has_pic` tinyint(1) DEFAULT NULL COMMENT '是否有图片 评价搜索用',
  `pic_urls` varchar(4096) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '评价文件集合/图片&视频, 英文分号；分隔',
  `description` varchar(128) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '评价描述，比如：用户XX小时后追评',
  `show_status` varchar(32) COLLATE utf8mb4_bin DEFAULT NULL COMMENT '展示状态',
  `parent_evaluation_id` bigint(20) DEFAULT NULL COMMENT '父评价ID',
  `create_date` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `write_date` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  `create_uid` bigint(20) DEFAULT NULL COMMENT '创建人',
  `write_uid` bigint(20) DEFAULT NULL COMMENT '更新人',
  `is_deleted` bigint(20) DEFAULT '0' COMMENT '逻辑删除',
  `down_date` datetime DEFAULT NULL COMMENT '置底时间',
  `top_date` datetime DEFAULT NULL COMMENT '置顶时间',
  `is_top` tinyint(1) DEFAULT NULL COMMENT '是否置顶',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_bin COMMENT='下单确认收货后对商品评价';